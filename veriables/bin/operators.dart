void main() {
  // $ Бул озгормонун аталышын алуучу символ,
  // {} Бул эки санды окуучу символ
  // ++ орератору increment
  // -- оператору decrement
  print("10 + 8 =${10 + 8}");
  print("10 - 8 =${10 - 8}");
  print("10 * 8 =${10 * 8}");
  print("10 / 8 =${10 / 8}");
  int a = 11;
  print(--a);
}